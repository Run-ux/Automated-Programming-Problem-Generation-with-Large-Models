from __future__ import annotations

from .._common import SOLVE_INTERFACE_RULE, generated_problem_section, json_contract


FIXED_WRONG_CATEGORIES = (
    "目标/输出义务误读",
    "核心约束简化",
    "invariant 误读",
    "边界/最小性错误",
    "复杂度/规模误判",
)


_CATEGORY_REQUIREMENTS = {
    "目标/输出义务误读": """你是一名算法竞赛错误解法生成专家，熟悉真实选手在阅读题面时常见的目标误读、输出格式误解和评价标准偏差。
请生成一份“真实选手因误读目标或输出义务而写出的错误解法”。
错误应来源于对“要求输出什么”“优化目标是什么”“是否需要构造/计数/判定”等内容的误解。""",
    "核心约束简化": """你是一名算法竞赛错误解法生成专家，擅长模拟选手将复杂约束错误简化后写出的看似合理但不完整的解法。
请生成一份“真实选手因简化核心约束而写出的错误解法”。
错误解法应体现选手忽略、弱化或局部化某个关键约束；不能直接无视所有条件，应保留题目主要结构，只在关键约束上出现偏差。""",
    "invariant 误读": """你是一名算法竞赛错误解法生成专家，熟悉不变量、状态维护、单调性、守恒关系和递推条件被误读后产生的典型错误。
请生成一份“真实选手因误读 invariant 而写出的错误解法”。
错误解法应体现对状态不变量、必要条件、保持条件或等价关系的错误理解，且有明确算法思路，不能只是漏写判断。""",
    "边界/最小性错误": """你是一名算法竞赛错误解法生成专家，擅长模拟真实选手在边界情况、最小规模、极端输入和初始化条件上犯错的代码。
请生成一份“真实选手因边界或最小性理解错误而写出的错误解法”。
错误解法应在一般情况中看起来正确，但在最小输入、空状态、单元素、相等值、极端值或临界条件下失败。""",
    "复杂度/规模误判": """你是一名算法竞赛错误解法生成专家，熟悉真实选手因低估输入规模、误判复杂度或使用不可扩展算法而产生的错误提交。
请生成一份“真实选手因复杂度或规模误判而写出的错误解法”。
错误解法应在小数据上正确或近似正确，但在最大规模下超时、超内存或退化。""",
}


def build_system_prompt() -> str:
    return (
        "你是算法竞赛错误解法生成专家，负责模拟真实选手提交的、逻辑自洽但存在指定策略缺陷的 Python 解法。"
        "不要生成无意义、随机、语法错误或刻意破坏的代码。"
        "最终只输出单个 JSON 对象。"
    )


def build_user_prompt(artifact: dict, category: str) -> str:
    if category not in FIXED_WRONG_CATEGORIES:
        raise ValueError("未知固定错误解类别: " + str(category))

    return "\n\n".join(
        [
            _CATEGORY_REQUIREMENTS[category],
            generated_problem_section(artifact),
            f"""# 生成要求
- 错误策略必须属于：{category}。
- 错误解法应像真实选手提交的代码，而不是刻意破坏的代码。
- 代码整体逻辑应自洽，能通过部分样例或弱数据。
- 不要写明显无意义、随机或语法错误的代码。
- 不要暴露“这是故意错误解法”。
- 输出完整可运行代码，语言使用 Python。
- {SOLVE_INTERFACE_RULE}
- solve 接收完整输入字符串，返回该错误解法认为正确的输出字符串。
- 代码只使用标准库，不依赖第三方库。
- 关键逻辑可以添加自然的中文注释，但不要写“错误”“故意”“测试用错误解”等暴露意图的注释。""",
            json_contract(
                """
{
  "code": "完整 Python 代码字符串，必须实现 solve(input_str: str) -> str"
}
"""
            ),
        ]
    )

